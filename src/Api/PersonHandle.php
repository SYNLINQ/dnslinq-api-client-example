<?php
namespace App\Api;

class PersonHandle
{
    /**
     *  The PersonHandle E-Mail Attribute
     * @var string
     */
    public $email;
    /**
     *  The PersonHandle First Name Attribute
     * @var string
     */
    public $firstname;
    /**
     *  The PersonHandle Last Name Attribute
     * @var string
     */
    public $lastname;
    /**
     *  The PersonHandle Organisation Attribute
     * @var string
     */
    public $organisation;
    /**
     *  The PersonHandle Street Attribute
     * @var string
     */
    public $street;
    /**
     *  The PersonHandle Street Number Attribute
     * @var string
     */
    public $number;
    /**
     *  The PersonHandle Post-Code Attribute
     * @var string
     */
    public $postcode;
    /**
     *  The PersonHandle City Attribute
     * @var string
     */
    public $city;
    /**
     *  The PersonHandle Region Attribute
     * @var string
     */
    public $region;
    /**
     *  The PersonHandle Country Attribute
     * @var string
     */
    public $country;
    /**
     *  The PersonHandle Country of Birth Attribute
     *  MUST BE ISO-3166-ALPHA-2 FORMAT for Germany - DE
     * @var string
     */
    public $countryofbirth;
    /**
     *  The PersonHandle Phone Attribute
     *  MUST BE E164 https://de.wikipedia.org/wiki/E.164
     * @var string
     */
    public $phone;
    /**
     *  The PersonHandle Fax Attribute
     * @var string
     */
    public $fax;
    /**
     *  The PersonHandle IDCARD Attribute
     * @var string
     */
    public $idcard;
    /**
     *  The PersonHandle IDCARD IssueDate Attribute
     * @var string
     */
    public $idcardissuedate;
    /**
     *  The PersonHandle IDCARD Authority Attribute
     * @var string
     */
    public $idcardauthority;
    /**
     *  The PersonHandle DateofBirth Attribute
     * @var string
     */
    public $dateofbirth;
    /**
     *  The PersonHandle RegionofBirth Attribute
     * @var string
     */
    public $regionofbirth;
    /**
     *  The PersonHandle PlaceofBirth Attribute
     * @var string
     */
    public $placeofbirth;
    /**
     *  The PersonHandle Regisrationnumber Attribute
     * @var string
     */
    public $registrationnumber;
}